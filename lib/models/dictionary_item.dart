  final String id;
  final String word;
  final String category;
  final String definition;
  final String videoUrl;
  final String imageUrl;

  DictionaryItem({
    required this.id,
    required this.word,
    required this.category,
    required this.definition,
    required this.videoUrl,
    required this.imageUrl,
  });
}
